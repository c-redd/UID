    private void jButtonUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUPDATEActionPerformed
        
         //Update the table
        DefaultTableModel tbModel = (DefaultTableModel)jTableNRC.getModel();
        if(jTableNRC.getSelectedRow()==-1){
            if(jTableNRC.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Table is Empty");
            }else{
                JOptionPane.showMessageDialog(this, "You must enter all");
            }
        }else{
            tbModel.setValueAt(jTextFieldRegNO.getText(),jTableNRC.getSelectedRow(),0);
            tbModel.setValueAt(jTextFieldRD.getText(),jTableNRC.getSelectedRow(),1);
            tbModel.setValueAt(jTextFieldPOB.getText(),jTableNRC.getSelectedRow(),2);
            tbModel.setValueAt(jTextFieldDistrict.getText(),jTableNRC.getSelectedRow(),3);
            tbModel.setValueAt(jTextFieldVillage.getText(),jTableNRC.getSelectedRow(),4);
            tbModel.setValueAt(jTextFieldChief.getText(),jTableNRC.getSelectedRow(),5);
    }//GEN-LAST:event_jButtonUPDATEActionPerformed
    }
    private void jButtonDELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDELETEActionPerformed
        // TODO add your handling code here:
        //Delete the information in table
        DefaultTableModel tbModel = (DefaultTableModel)jTableNRC.getModel();
        if(jTableNRC.getSelectedRow()==-1){
            if(jTableNRC.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Table is Empty");
            }else{
                JOptionPane.showMessageDialog(this, "You must enter all");
            }
        }else{
            tbModel.removeRow(jTableNRC.getSelectedRow());
    }//GEN-LAST:event_jButtonDELETEActionPerformed
    }
    private void jButtonSAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSAVEActionPerformed
        // TODO add your handling code here:
        if(jTextFieldRegNO.getText().equals("")||jTextFieldRD.getText().equals("")||jTextFieldPOB.getText().equals("")||jTextFieldDistrict.getText().equals("")||jTextFieldVillage.getText().equals("")||jTextFieldChief.getText().equals("")){
            // code for left blank fields
            JOptionPane.showMessageDialog(this, "Please Enter All Data!");

        }else{
            // if all data is enter
            //store enter data into string array DATA
            String data[] = {jTextFieldRegNO.getText(), jTextFieldRD.getText(), jTextFieldPOB.getText(), jTextFieldDistrict.getText(), jTextFieldVillage.getText(), jTextFieldChief.getText()};

            DefaultTableModel tbModel = (DefaultTableModel)jTableNRC.getModel();
            //add string array data
            tbModel.addRow(data);// row added
            //sucessfully added messge
            JOptionPane.showMessageDialog(this, "Add Data Successfully!");
            jTextFieldRegNO.setText("");
            jTextFieldPOB.setText("");
            jTextFieldRD.setText(null);
            jTextFieldDistrict.setText("");
            jTextFieldVillage.setText("");
            jTextFieldChief.setText("");
    }//GEN-LAST:event_jButtonSAVEActionPerformed
    }
